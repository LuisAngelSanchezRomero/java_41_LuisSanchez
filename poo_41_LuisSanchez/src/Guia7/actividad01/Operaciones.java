package Guia7.actividad01;
// Actividad 1
public class Operaciones {
    
    public double result;
    
    public void calsuma(double n1, double n2){
        result = n1 + n2;
    }
    
    public void calresta(double n1, double n2){
        result = n1 - n2;
    }
    
    public void calMultiplica(double n1, double n2){
        result = n1 * n2;
    }
    
    public void calDivide(double n1, double n2){
        result = n1 / n2;
    }
    
}
